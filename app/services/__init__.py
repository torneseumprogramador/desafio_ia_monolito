# Services (Lógica de Negócio)
# 
# Os services contêm a lógica de negócio da aplicação.
# Eles são chamados pelos controllers e interagem com os models.
# 
# Exemplo:
# - UserService: lógica para criar, atualizar, deletar usuários
# - ProductService: lógica para gerenciar produtos
# - EmailService: lógica para enviar emails

